package variables;

public class Variables {

    public static void main(String[] args) {
        System.out.println("============= Variables in Java =============");

        /*
            En Java, une variable est utilisée pour stocker des données qui peuvent être utilisées et manipulées dans un programme.
            Voici les points essentiels à connaître sur les variables en Java :
            1 - Pour déclarer une variable, vous devez spécifier son type et son nom.
            2 - Vous pouvez assigner une valeur à une variable au moment de sa déclaration ou plus tard.
        */

        int number1 = 6;
        int number2 = 4;
        int result = number1 + number2;


        System.out.println(result);
    }
}
